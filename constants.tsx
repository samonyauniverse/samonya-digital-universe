// Helper to generate consistent pseudo-random numbers
const seededRandom = (seed: number) => {
    const x = Math.sin(seed++) * 10000;
    return x - Math.floor(x);
};

const generateArticles = (brandId: BrandId, count: number, startId: number): ContentItem[] => {
    const items: ContentItem[] = [];
    
    const subjects: Record<string, string[]> = {
        [BrandId.MILELE]: ['The Lost Gospel', 'Hidden Angels', 'Faith in Crisis', 'The Silent Prayer', 'Divine Intervention', 'Ancient Scriptures Decoded', 'The Power of Fasting', 'Miracles Today'],
        [BrandId.FILMS]: ['Forbidden Love (Romance)', 'Cyber Agent 009 (Action)', 'The Last Stand (Thriller)', 'Love in Paris (Romance)', 'Robot Wars (Action)', 'The Ghost of Tomorrow (Horror)', 'Speed Chase (Action/Romance)'],
        [BrandId.KIDS]: ['Learning Colors', 'The Brave Little Lion', 'Counting Stars', 'ABC Adventure', 'Why is the Sky Blue?', 'The Friendly Dragon', 'Magic Numbers', 'Space Explorer'],
        [BrandId.TV]: ['The AI Revolution', 'Climate Change Reality', 'Political Secrets Exposed', 'History of Kings', 'Global Sports Recap', 'Tech Future 2050', 'The Great Depression Truth', 'Nature\'s Fury'],
        [BrandId.SKY]: ['Top 10 Celeb Secrets', 'Meme of the Year', 'Viral TikTok Trends', 'Comedy Gold', 'Rapper Lifestyle', 'Hollywood Uncut', 'Fashion Fails', 'Best Jokes 2024'],
        [BrandId.MOTIVATION]: ['Inspire'], // Handled separately
        [BrandId.ACADEMY]: ['Research'], // Handled separately
        [BrandId.DREAM_EYE]: ['Lucid Dreaming Guide', 'Nightmares Explained', 'Astral Projection'],
        [BrandId.SAMONYA_AI]: ['AI Consciousness', 'Future of Robotics', 'Human-AI Friendship'],
        [BrandId.COMEDY]: ['Funny Prank', 'Office Fails'],
        [BrandId.BLOG]: ['Blog Post'],
        [BrandId.LIVE]: ['Live Stream'],
        [BrandId.BUSINESS_MATRIX]: ['Startup Guide', 'Market Trends 2025', 'Crypto Future', 'Leadership 101']
    };

    const tagsList = ['Trending', 'Viral', 'New', 'Must Watch', 'Exclusive', 'Hidden Truth', 'AI Generated'];

    for (let i = 0; i < count; i++) {
        const seed = startId + i;
        const brandSubjects = subjects[brandId] || ['General'];
        const subject = brandSubjects[Math.floor(seededRandom(seed) * brandSubjects.length)];
        const title = `${subject}: Part ${i + 1}`;
        
        items.push({
            id: `gen-${brandId}-${i}`,
            title: title,
            description: `A 5000+ word deep dive into ${subject}. Generated by AI, this article exposes the hidden truths and realities of the topic.`,
            thumbnail: `https://picsum.photos/seed/${brandId}${i}/800/450`,
            brandId: brandId,
            type: 'article',
            isPremium: i % 5 === 0, // Every 5th item is premium
            date: new Date(Date.now() - Math.floor(seededRandom(seed) * 10000000000)).toISOString(),
            tags: [tagsList[Math.floor(seededRandom(seed) * tagsList.length)], 'AI', 'Deep Dive'],
            views: Math.floor(seededRandom(seed) * 50000) + 500,
            interactions: { 
                likes: Math.floor(seededRandom(seed) * 2000), 
                loves: Math.floor(seededRandom(seed) * 500), 
                dislikes: Math.floor(seededRandom(seed) * 50), 
                comments: Math.floor(seededRandom(seed) * 100) 
            },
            commentList: []
        });
    }
    return items;
};

